package circulo;

public class CriaCirculo {
    private double raio;
    
    public CriaCirculo(double raio){
        this.raio = raio;
    }

    public double getRaio() {
        return raio;
    }

    public void setRaio(double raio) {
        this.raio = raio;
    }
    
    public double calcularArea(){
        return Math.PI * raio * raio;
    }
}
